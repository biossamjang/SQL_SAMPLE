WITH TEMP AS
(
SELECT A.TABLE_NAME
      ,A.COMMENTS
      ,B.OWNER
  FROM ALL_TAB_COMMENTS A
      ,ALL_SYNONYMS B
 WHERE A.OWNER = 'ANAM_CDW'
   AND  A.TABLE_TYPE = 'TABLE'
   AND A.TABLE_NAME = B.SYNONYM_NAME (+)
--   AND ORG_OBJECT_OWNER = 'KUMADM'
   AND B.OWNER <> 'PUBLIC'
)

SELECT 'DROP SYNONYM '||T.OWNER||'.'||T.TABLE_NAME||';'
  FROM TEMP T
  ;